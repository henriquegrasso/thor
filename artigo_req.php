				<h2>Qual a diferença entre Requisitos e Restrições?</h2>				
				
			  <article>				
				<br><p class = "texto_art">Na gestão e documentação de requisitos é muito comum e recorrente a dúvida entre requisitos e restrições. 
				 Dessa maneira, neste texto iremos diferenciá-los.</p><br>
				
				<p class = "texto_art"> <b>Requisito:</b> Condição que se deve satisfazer para alcançar certo fim; Exigência de ordem 
					legal para que determinado processo possa ter andamento</p>
					<p class = "texto_art">Segundo o Guia PMBOK: Uma condição ou capacidade que um sistema / produto deve atender ou possuir para 
					satisfazer um contrato, uma norma, uma especificação ou outros documentos impostos formalmente. 
					Os requisitos incluem necessidades, desejos e expectativas quantificados e documentados das partes interessadas.</p><br>
				<p class = "texto_art"><b>Está relacionado às funcionalidades do sistema!!</b></p><br>

				<p class = "texto_art"> <b>Restrição:</b> Ação ou efeito de restringir ou restringir-se; Circunstância ou estado restritivo;
				 que impõe limite; que se apresenta de modo condicionante.</p>
				 <p class = "texto_art"> Restrições são fatores internos e externos que limitam as opções da equipe de gerenciamento do projeto. 
				 Em geral são requisitos obrigatórios, impostos pelo cliente ou outra parte interessada, que são oriundos de registros de requisitos
				  e são incluídos na declaração do escopo com destaque especial.</p><br>
				<p class = "texto_art"><b>Está relacionada ao processo de gestão do projeto!!</b></p><br>
